import math

value = 3

#Square root
sqrtVal = math.sqrt(value)
print(sqrtVal)

print(sqrtVal**2)# sqrtVal ^2

#exp
expVal = math.exp(value)# e^Val
print(expVal)

#Factorial - silnia
factVal  = math.factorial(value)
print(factVal)

#Sin - sinus in radius
sinVal = math.sin(value)
print(sinVal)

#Floor - zaokrąglanie do niższej całkowitej
pi = 3.99
floorVal = math.floor(pi)
print(floorVal)

#ceiling - zaokrągla do wyższej całkowitej
ceilingVal = math.ceil(pi)
print(ceilingVal)

